const fs = require("fs");
const path = require("path");

/**
 * Generate src/registry.ts with fetch-based entries pointing to the API.
 *
 * Reads icon names from a source folder, strips .json extensions, and
 * creates registry entries that fetch from the IconTree API at runtime.
 *
 * Usage:
 *   node scripts/generate-registry.js <source-folder>
 *
 * The source folder should contain .json files whose names are the icon IDs.
 *
 * Example:
 *   node scripts/generate-registry.js D:/Downloads/Iconaassets/public/icons
 */

const API_BASE = "https://icontree.cosmoscope.workers.dev/icon/use";

const sourceArg = process.argv[2];
if (!sourceArg) {
  console.error("Usage: node scripts/generate-registry.js <source-folder>");
  console.error("  e.g. node scripts/generate-registry.js D:/Downloads/Iconaassets/public/icons");
  process.exit(1);
}
const sourceFolder = path.resolve(sourceArg);

// ── Read icon names ──────────────────────────────────────────────
let files;
try {
  files = fs.readdirSync(sourceFolder).filter((f) => f.endsWith(".json")).sort();
} catch (err) {
  console.error(`Cannot read folder: ${sourceFolder}`);
  console.error(err.message);
  process.exit(1);
}

if (files.length === 0) {
  console.error(`No .json files found in ${sourceFolder}`);
  process.exit(1);
}

// Strip all trailing .json suffixes
function cleanName(filename) {
  let name = filename;
  while (name.endsWith(".json")) name = name.slice(0, -5);
  return name;
}

const names = files.map(cleanName).filter(Boolean);

// ── Write registry.ts ────────────────────────────────────────────
const entries = names
  .map(
    (name) =>
      `  "${name}": () =>\n    fetch("${API_BASE}/${name}.json").then((r) => {\n      if (!r.ok) throw new Error("Failed to fetch icon: ${name}");\n      return r.json();\n    }),`
  )
  .join("\n");

const content = [
  "// Auto-generated by scripts/generate-registry.js",
  "// Source folder: " + sourceFolder,
  "// Icon data is fetched from the API at runtime — no local copies.",
  `export const registry: Record<string, () => Promise<unknown>> = {`,
  entries,
  "};",
  "export type IconId = keyof typeof registry;",
  "",
].join("\n");

const registryPath = path.resolve(__dirname, "../src/registry.ts");
fs.writeFileSync(registryPath, content, "utf-8");

console.log(`\n✓ Registry generated at src/registry.ts`);
console.log(`  ${names.length} icon(s) — runtime fetch from API`);
